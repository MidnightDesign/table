<?php

namespace Midnight\Table\Model\Column;

class Column implements ColumnInterface
{
    /**
     * @var string
     */
    private $label;

    /**
     * @return string
     */
    public function getLabel()
    {
        return $this->label;
    }

    /**
     * @param string $label
     */
    public function setLabel($label)
    {
        $this->label = $label;
    }
}
